function day(n) {
    switch (n) {
        case 1:
            console.log("Monday");
            break;
        case 2:
            console.log("Tuesday");
            break;
        case 3:
            console.log("Wdnesday");
            break;
        case 4:
            console.log("Thursday");
            break;
        case 5:
            console.log("Friday");
            break;
        case 6:
            console.log("Saturday");
            break;
        case 7:
            console.log("Sunday");
            break;
        default:
            console.log("Enter valid input");
            break;

    }
    return ""
}

console.log(day(1));
console.log(day(4));
console.log(day("ghsdchjh"));





